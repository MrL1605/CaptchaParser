    public String getCaptcha() {

        bm = BitmapFactory.decodeFile(IMAGE_FILE.getPath());
        int height = bm.getHeight();
        int width = bm.getWidth();

        pixel_map = new String[height][width];
        for (int j = 0; j < height; j++) {
            pixel_map[j] = new String[width];
        }

        for (int y = 0; y < height; y++) {
            for (int x = 0; x < width; x++) {
                int pixel = bm.getPixel(x, y);
                if (pixel == -289) {
                    pixel = 0;
                } else {
                    pixel = 1;
                }
                pixel_map[y][x] = pixel + "";
                if (y != 0 && y != 24) {
                    int tmp1 = bm.getPixel(x, y + 1);
                    int tmp2 = bm.getPixel(x, y - 1);
                    if (tmp1 == -289 && tmp2 == -289 && pixel == 1) {
                        pixel_map[y][x] = 0 + "";
                    }
                }
            }
        }

        int x_off = 20, y_off = 2;

        String[][] pixel_cpy = new String[pixel_map.length][];
        for (int k = 0; k < pixel_map.length; k++) {
            pixel_cpy[k] = new String[pixel_map[k].length];
            System.arraycopy(pixel_map[k], 0, pixel_cpy[k], 0, pixel_map[k].length);
        }

        ArrayList<Integer> skip_start = new ArrayList<>(),
                skip_end = new ArrayList<>();
        Map<Integer, String> sorter = new HashMap<>();

        int f;
        for (int l = 0; l < 36; l++) {
            String[][] mask = keys.get(order[l]);
            f = 0;
            for (int y = y_off; y < 24; y++) {
                for (int x = x_off; x < 129; x++) {
                    if (!skip(skip_start, skip_end, x)) {
                        if (match_img(x, y, pixel_cpy, mask) == 1) {
                            skip_start.add(x);
                            skip_end.add(x + mask[0].length);
                            sorter.put(x, order[l]);
                            f += 1;
                        }
                    }
                }
            }
            if (f == 6) {
                break;
            }
        }
        // Sorter
        Set<Integer> keys = sorter.keySet();
        List<Integer> keyColList = new ArrayList<>();
        keyColList.addAll(keys);
        Collections.sort(keyColList);

        String captcha = "";
        for (int keyIndex : keyColList) {
            captcha += sorter.get(keyIndex);
        }
        return captcha;
    }

    int match_img(int rx, int ry, String[][] pix1, String[][] mask1) {
        int flag = 1, break_flag = 0, count = 0;
        for (int y_ = 0; y_ < mask1.length; y_++) {
            for (int x_ = 0; x_ < mask1[y_].length; x_++) {
                try {
                    if (mask1[y_][x_].equals("1")) {
                        if (pix1[ry + y_][rx + x_].equals("1")) {
                            count++;
                        } else {
                            flag = 0;
                            break_flag = 1;
                            break;
                        }
                    }
                } catch (Exception e) {
                    flag = 0;
                    break_flag = 1;
                    break;
                }
            }
            if (break_flag == 1) {
                break;
            }
        }
        if (count == 0) flag = 0;
        return flag;
    }

    boolean skip(ArrayList<Integer> start, ArrayList<Integer> end, int x) {
        for (int i = 0; i < start.size(); i++) {
            if (x >= start.get(i) && x <= end.get(i)) {
                return true;
            }
        }
        return false;
    }

